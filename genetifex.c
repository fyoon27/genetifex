#include <stdlib.h>
#include <stdio.h>
#include <stdarg.h>

#include "genetifex.h"

xcb_connection_t * c;
xcb_screen_t * screen;
xcb_window_t window;

void __attribute((noreturn)) die(const char const * message, ...)
{
    va_list args;

    va_start(args, message);
    fputs("FATAL: ", stderr);
    vfprintf(stderr, message, args);
    fputc('\n', stderr);
    va_end(args);

    cleanup();

    exit(EXIT_FAILURE);
}

